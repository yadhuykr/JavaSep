package classes;

public class Multiplication {

    public float doMultiplication(float x, float y){
        return x*y;
    }

    public float doMultiplication(float x, float y, float z){
        return x*y*z;
    }

    public float doMultiplication(float w,float x, float y, float z){
        return w*x*y*z;
    }


}
