package classes;

public class Subtraction {

    public float doSubtraction(float x, float y){
        return x-y;
    }

    public float doSubtraction(float x, float y, float z) {
        return x-y-z;
    }

    public float doSubtraction(float w,float x, float y, float z) {
        return w-x-y-z;
    }
}
